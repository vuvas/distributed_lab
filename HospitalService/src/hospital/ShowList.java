    public ShowList() {
        initComponents();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.getDataVector().removeAllElements();
        revalidate();
        try {
            String url = "jdbc:mysql://localhost:3306/hospitalservice";
            Properties props = new Properties();
            props.setProperty("user", "root");
            props.setProperty("password", "123456");
            Connection connection = DriverManager.getConnection(url, props);
            PreparedStatement statement = connection.prepareStatement("SELECT * FROM patientinformation");
            ResultSet rs = statement.executeQuery();
            
            while (rs.next()) {
                String[] str = {rs.getString("id"),rs.getString("name"),rs.getString("age"),rs.getString("gender"),rs.getString("address")};
                model.addRow(str);
            } 
        } catch (SQLException ex) {
           
        }
        
    }

